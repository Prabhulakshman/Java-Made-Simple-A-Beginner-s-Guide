package PreJava;

public class Switch {

	public static void main(String[] args) {
		int choice=10;
     switch(choice)
     {
     case 1:
     
    	 System.out.println("Choice 1");
    	 break;
     
     case 2:
     
    	 System.out.println("Choice 2");
    	 break;
     
     default:
     
    	 System.out.println("Invalid Choice");
     }

	}

}
