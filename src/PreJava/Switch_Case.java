package PreJava;

public class Switch_Case {

	public static void main(String[] args) {
		char grade='p';
		switch(grade)
		{
		case 'a':
			 System.out.println("Grade a");
	    	 break;
		case 'b':
			 System.out.println("Grade b");
	    	 break;
	    default:
	    	 System.out.println("Invalid Grade");
	    	 
		}

	}

}
