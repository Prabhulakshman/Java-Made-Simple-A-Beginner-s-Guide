package CoreJava;
//Super Class
//Method Overriding means changing only the method definition for the already having method declaration.
//To achieve method overriding Inheritance is mandatory.
public class Method_Overriding_Super {
public static void test()
{
	System.out.println("test() executed");
}
public  void demo()
{
	System.out.println("demo() executed");
}
private static void sample()
{
	System.out.println("sample() executed");
}
final static void nothing()
{
	System.out.println("nothing() executed");
}
}
