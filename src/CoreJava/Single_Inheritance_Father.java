package CoreJava;
//Single Inheritance Example
//Parent Class or Base Class or Super Class - Father
//Child Class or Derived Class or Sub Class - Child
public class Single_Inheritance_Father {
//Fathers Properties
	double bank_balance=1500000.0;
	public void bike()
	{
		System.out.println("Dad's Bike");
	}
	public void car()
	{
		System.out.println("Dad's Car");
	}
	public void home()
	{
		System.out.println("Dad's Home");
	}
}
